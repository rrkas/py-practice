a = 10
b = 5

print(a + b)  # 15
print(a - b)  # 5
print(a * b)  # 50
print(a / b)  # 2.0         # divide (float)
print(a // b)  # 2           # int divide
print(a ** b)  # 100_000     # power x**y = x^y
print(a % b)  # 0           # reminder
print()

